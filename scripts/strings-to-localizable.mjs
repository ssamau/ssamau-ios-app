#!/usr/bin/env node
// strings-to-localizable.mjs
//
// Ports the web app's i18n catalog (assets/js/lib/strings/{ar,en}.js) into
// Localizable.strings files for the iOS app. Run after the web catalog
// changes — see spec §17.3.
//
// Usage:
//   node scripts/strings-to-localizable.mjs [WEB_REPO_PATH]
//
// Defaults WEB_REPO_PATH to "~/Desktop/SSAMAU Website/ssamau-site".
// Writes to:
//   SSAMAU/SSAMAU/Resources/Localizable.strings              (English)
//   SSAMAU/SSAMAU/Resources/ar.lproj/Localizable.strings     (Arabic)

import { writeFile, mkdir } from 'node:fs/promises';
import { dirname, resolve } from 'node:path';
import { fileURLToPath, pathToFileURL } from 'node:url';
import { homedir } from 'node:os';

const here = dirname(fileURLToPath(import.meta.url));
const iosRoot = resolve(here, '..');

const webRepoArg = process.argv[2];
const webRepo = webRepoArg
  ? resolve(webRepoArg)
  : resolve(homedir(), 'Desktop/SSAMAU Website/ssamau-site');

const sources = [
  {
    lang: 'en',
    src: resolve(webRepo, 'assets/js/lib/strings/en.js'),
    // Canonical Xcode layout — English under en.lproj, not at root.
    // Putting it at the bundle root made iOS treat it as a base fallback
    // but didn't always trigger ar.lproj lookup. en.lproj is unambiguous.
    out: resolve(iosRoot, 'SSAMAU/SSAMAU/Resources/en.lproj/Localizable.strings'),
  },
  {
    lang: 'ar',
    src: resolve(webRepo, 'assets/js/lib/strings/ar.js'),
    out: resolve(iosRoot, 'SSAMAU/SSAMAU/Resources/ar.lproj/Localizable.strings'),
  },
];

function escapeValue(value) {
  // Localizable.strings escaping. Source values may contain backslash,
  // double-quote, newline, tab. {var} interpolation tokens pass through
  // unchanged — ErrorLocalization handles them at runtime.
  return value
    .replace(/\\/g, '\\\\')
    .replace(/"/g, '\\"')
    .replace(/\n/g, '\\n')
    .replace(/\t/g, '\\t');
}

// iOS-only keys (tab labels, etc.) — merged on top of the web catalog.
async function loadIosOnly() {
  const path = resolve(here, 'ios-only-strings.json');
  const { readFile } = await import('node:fs/promises');
  const raw = await readFile(path, 'utf8');
  const obj = JSON.parse(raw);
  delete obj._comment;
  return obj; // { key: { en, ar } }
}

async function convert({ lang, src, out, iosOnly }) {
  const mod = await import(pathToFileURL(src).href);
  const catalog = mod.default;
  if (!catalog || typeof catalog !== 'object') {
    throw new Error(`No default export in ${src}`);
  }

  // iOS-only entries override web ones on key collision. (Today there are
  // no collisions; this just keeps the merge deterministic if one appears.)
  const merged = { ...catalog };
  for (const [key, langs] of Object.entries(iosOnly)) {
    if (typeof langs?.[lang] === 'string') {
      merged[key] = langs[lang];
    }
  }

  const keys = Object.keys(merged).sort();
  const iosOnlyKeys = new Set(Object.keys(iosOnly));
  const lines = [
    '/*',
    `  Localizable.strings  (${lang})`,
    `  Auto-generated from ${src.replace(homedir(), '~')}`,
    `  + scripts/ios-only-strings.json (iOS-only overlay).`,
    `  Generated by scripts/strings-to-localizable.mjs — do not hand-edit.`,
    '*/',
    '',
  ];
  for (const key of keys) {
    const value = merged[key];
    if (typeof value !== 'string') {
      console.warn(`Skipping non-string value at key "${key}"`);
      continue;
    }
    const marker = iosOnlyKeys.has(key) ? '  // ios-only' : '';
    lines.push(`"${key}" = "${escapeValue(value)}";${marker}`);
  }
  lines.push('');

  await mkdir(dirname(out), { recursive: true });
  await writeFile(out, lines.join('\n'), 'utf8');
  console.log(`Wrote ${keys.length} keys (${iosOnlyKeys.size} iOS-only) → ${out.replace(iosRoot + '/', '')}`);
}

const iosOnly = await loadIosOnly();
for (const source of sources) {
  await convert({ ...source, iosOnly });
}
